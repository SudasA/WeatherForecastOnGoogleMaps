package com.example.weatherforecast.data;

public class Coord {

    private float lat;
    private float lon;

    public float getLat() {
        return lat;
    }

    public void setLat(float lat) {
        this.lat = lat;
    }

    public float getLng() {
        return lon;
    }

    public void setLng(float lon) {
        this.lon = lon;
    }
}
